class IconElement extends HTMLElement {
	constructor() {
		super();
		this.attachShadow({ mode: "open" }); // 웹 컴포넌트의 Shadow DOM 사용
	}

	connectedCallback() {
		const name = this.getAttribute("name") || "default";
		const fill = this.getAttribute("fill") || null;
		const type = this.getAttribute("type") || "svg";
		const device = this.getAttribute("device") || null;
		this.render(name, fill, type, device);
	}

	render(name, fill, type, device) {
		const imgOff = type === 'img' ? `../_assets/user/images/icon/${name}.svg` : "";
		const imgOn = type === 'img' ? `../_assets/user/images/icon/${name}_on.svg` : "";

		this.shadowRoot.innerHTML = `
			<style>
				svg, .icon {
					width: 100%;
					height: 100%;
					display: block;
				}
				svg {
					${!fill ? `fill: currentColor;` : ""}
				}
				.icon {
					background: url('${imgOff}') no-repeat center/contain;
				}
				:host-context(a:hover) .icon,
				:host-context(a:focus-visible) .icon,
				:host-context(button:hover) .icon,
				:host-context(button:focus-visible) .icon {
					background-image: url('${imgOn}')
				}

				${device === "pc" ? `
          @media (max-width: 1023.98px) {
            :host-context(a:hover) .icon,
						:host-context(a:focus-visible) .icon,
						:host-context(button:hover) .icon,
						:host-context(button:focus-visible) .icon {
							background-image: url('${imgOff}')
						}
          }
        ` : ""}
			</style>
			${type === "img" ? `<span class="icon"></span>` : icons[name] || icons["default"]}
		`;
	}
}

customElements.define("c-icon", IconElement);